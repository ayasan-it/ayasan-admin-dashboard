export type Translations = typeof T.en;

const LangCtx = createContext<{
  lang: Lang;
  setLang: (l: Lang) => void;
  t: Translations;
}>({ lang: "en", setLang: () => {}, t: T.en });

export function LangProvider({ children }: { children: ReactNode }) {
  const stored = (typeof localStorage !== "undefined" ? localStorage.getItem("ayasan_lang") : null) as Lang | null;
  const [lang, setLangState] = useState<Lang>(stored ?? "en");

  function setLang(l: Lang) {
    setLangState(l);
    localStorage.setItem("ayasan_lang", l);
  }

  return (
    <LangCtx.Provider value={{ lang, setLang, t: T[lang] as Translations }}>
      {children}
    </LangCtx.Provider>
  );
}

export function useT() {
  return useContext(LangCtx);
}
